;********************************************************************************
;                  二进制加法试验												*
; p1口八个灯作二进制加法。理解二进值的计算                                  	*
; 硬件连接：p1口8路指示灯														*
;																				*                 *
;********************************************************************************

     	ORG 	0000h	 ;程序从0000开始运行
     	AJMP	main
	    ORG	    0030h
main:	MOV	A  ,#0ffh
loop: 	MOV     p1,A	;全不亮
    	CALL    delay	;延时。
	    DEC	    A	    ;每次减一。
	    JMP     loop

delay:	MOV r5,#50	;延时。
d1: 	MOV r6,#40
d2:	MOV r7,#248
	DJNZ r7,$
	DJNZ r6,d2
	DJNZ r5,d1
	RET
END
